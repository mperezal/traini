        <p class="text">Today I came to share a great book: Sweller’s Cognitive Load Theory in Action by Oliver Lowell. If you are an instructional designer, this is a great addition to your library. In this book, we will find practical applications of the cognitive load theory. The cognitive load theory of John Sweller explains that the brain has to deal with multiple elements of information and difficult material, and it has to manipulate or process those different elements, and because of that working memory can struggle.<br><br> We think with a combination of information in the environment, long-term memory, and our limited working memory (the bottleneck of cognition). We overcome the limits of our working memory by chunking and automating information in long-term memory. The main goal is to minimize extraneous load and optimize the intrinsic load of content. And that it would be the objective of the instructional designer. The book is divided into three parts: Part I presents the cognitive load theory in its five principles. And Part II and Part III show practical applications.The most interesting part is the practical applications. So I share here a pair of tips on how to present information. Some of these tips could sound obvious but needs to be remembered. <li>Image and text together represent redundancy. If they both communicate the same.</li><li>Information should only be presented together in space and time if cant is understood in isolation and is essential rather than redundant.</li><br>Sometimes we find poor learning material and taking into account these tips you will reduce external cognitive load and increase the intrinsic load.It has been a pleasure to read it. I totally recommend it.</p>
