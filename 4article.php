This book has become a reference among L&D professionals and instructional designers. It encourages us to use learning techniques evidence-based: retrieval, interleaved practice, elaboration, etc. Among other problems, L&D deals with the formal education legacy, and sometimes this is the way we understand learning: lectures, magisterial classes, rereading. Certainly, we need to stop preparing PowerPoint or rereading content and call it learning, when this is only acquiring information.<br><br>

But the most important thing is, that learning has to be meaningful. Sometimes the worst issue is not HOW, instead WHAT FOR. Learning happens without a formal system. Learning departments are just bad-focused.We work as the school. And school as a concept started in the middle age, with religious sacred book reading as practice. And this system still evolves too slowly after 500 years of history. Make it stick, faces these well-known concerns with fresh air.<br><br>

So if you observe this situation and want to know more about learning techniques. Cognitive psychologists have been studying how we learn for 60 years, and this is a great book. I recommend it to you.<br><br>
